//arithmetic operator
let a = 45;
let b = 4;
console.log("a + b = ",a+b);
console.log("a - b = ",a-b);
console.log("a / b = ",a/b);
console.log("a * b = ",a*b);
console.log("a ** b = ",a**b);
console.log("a % b = ",a%b);
// console.log("a++ = ",a++);
// console.log("a-- = ",a--);
console.log("++a = ",++a);//46
console.log("--a = ",--a);//45

//comparison operators
let c = 6;
let d = "6";
console.log(c == d);
console.log(c != d);
console.log(c === d);
console.log(c !== d);
console.log(c > d);

//logical operators
let x = 5;
let y = 9;
console.log(x<y && x==5);
console.log(x<y || x==5);
console.log(!x<y && x==5);
