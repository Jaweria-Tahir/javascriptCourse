//template literals
let boy1 = "Harry";
let boy2 = "Haris";
let sentence = `${boy1} is a friend of ${boy2}`;
console.log(sentence);
//Escape Sequence Characters
let fruit = 'Bana\'na';//\'
console.log(fruit);
let fruit1 = 'Bana\nna';//n
console.log(fruit1);
let fruit2 = 'Bana\tna';//t
console.log(fruit2);
let fruit3 = 'Bana\rna';//\r
console.log(fruit3);